# This video demonstrates how you can have multiple elifs in a single conditional block

color = input("What's your favorite color?")
if color == 'purple':
        print('excellent choice!')
elif color == 'teal':
        print('not bad!')
elif color == 'seafoam':
        print('mediocre')
elif color == 'pure darkness':
        print('I like how you think')
else:
        print('YOU MONSTER!')